package Array;

public class EvenOddArray {

	public static void main(String[] args) {
		
		int arr[]= {3,23,29,8,12,10,22,27,6,};
		
		for(int i=0;i<arr.length;i++)
		{
			if(arr[i]%2==0)                                
			
			{
				System.out.println("Even elemnt is "+arr[i]+ "at position " +i);
			                               
			}
		}

	}

}
